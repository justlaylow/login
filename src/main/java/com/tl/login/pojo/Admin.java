package com.tl.login.pojo;

public class Admin {
	private int id;
	private String adm_count;
	private String adm_pass;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAdm_count() {
		return adm_count;
	}
	public void setAdm_count(String adm_count) {
		this.adm_count = adm_count;
	}
	public String getAdm_pass() {
		return adm_pass;
	}
	public void setAdm_pass(String adm_pass) {
		this.adm_pass = adm_pass;
	}
}
